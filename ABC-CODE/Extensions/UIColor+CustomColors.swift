import Foundation
import UIKit

extension UIColor
{
    class func countryThemeGrey() -> UIColor
    {
        return UIColor(red: 139.0/255.0, green: 139.0/255.0, blue: 139.0/255.0, alpha:1.0)
    }
    
    class func countryThemeBlack() -> UIColor
    {
        return UIColor(red: 31.0/255.0, green: 31.0/255.0, blue: 31.0/255.0, alpha:1.0)
    }
    
    class func countryOffWhite() -> UIColor
    {
        return UIColor(red: 250.0/255.0, green: 250.0/255.0, blue: 250.0/255.0, alpha:1.0)
    }
    
    class func loaderColor() -> UIColor
    {
        return UIColor(red: 150.0/255.0, green: 50.0/255.0, blue: 120.0/255.0, alpha:1.0)
    }
    
    class func areaGreen() -> UIColor
    {
        return UIColor(red: 48.0/255.0, green: 170.0/255.0, blue: 39.0/255.0, alpha:1.0)
    }
    
    class func populationRed() -> UIColor
    {
        return UIColor(red: 178.0/255.0, green: 23.0/255.0, blue: 23.0/255.0, alpha:1.0)
    }
    
}
